let expect = require("chai").expect;

const parse = input => {
  const programs = input.split('\n');
  return programs.map((program) => {
    let [id, neighbours] = program.split(' <-> ');
    neighbours = neighbours.split(',').map(x => x.trim())
    return {id, neighbours, traveled: false}
  })
}

const plumber = (programs) => {
  const travel = program => {
    program.neighbours.forEach(programId => {
      const currentProgram = programs.find(({id}) => id === programId);
      if (!currentProgram.traveled) {
        currentProgram.traveled = true;
        travel(currentProgram);
      }
    });
  }

  travel(programs[0]);

  const part1 = programs.reduce((sum, program) => sum + program.traveled, 0);

  let part2 = 1;
  while (!programs.every(({traveled}) => traveled)) {
    part2++;
    travel(programs.find(({traveled}) => !traveled));
  }

  return {part1, part2};
}


describe('Digital plumber', () => {
  it('should be correct with input', () => {
    const {part1, part2} = plumber(parse(input));
    expect(part1).to.equal(306);
    expect(part2).to.equal(200);

  });

  it('should be correct with demoInput', () => {
    const {part1, part2} = plumber(parse(demoInput));
    expect(part1).to.equal(6);
    expect(part2).to.equal(2);
  });

});


